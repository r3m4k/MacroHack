import pandas as pd
import numpy as np

def get_gva_monthly_dataframe() -> pd.DataFrame:
    """
    Загрузка и интерполяция квартальных данных валовой добавленной стоимости (GVA)
    на месячную частоту.
    Возвращает DataFrame с колонками Date (первый день месяца) и Value.
    """
    # Исходные данные (60 квартальных значений с 2011 по 2025)
    raw_numbers = [
        11329.5, 12397.8, 13592.2, 14596.1,
        13090.2, 14064.1, 15447.0, 16334.3,
        14171.7, 15183.3, 16672.3, 17694.0,
        14941.4, 16392.7, 17975.8, 19428.0,
        16207.8, 17822.8, 19595.7, 20994.2,
        16811.5, 18492.7, 20022.6, 21750.4,
        18525.8, 19815.3, 21411.0, 23145.2,
        20012.1, 22366.2, 24285.1, 26165.5,
        21909.9, 23991.4, 25504.1, 27081.8,
        22306.9, 21348.6, 25249.6, 28091.1,
        24781.3, 28596.2, 31655.5, 35767.8,
        32972.4, 34049.7, 35788.3, 39661.2,
        32979.7, 36589.1, 41845.6, 47562.1,
        39713.5, 43258.8, 46911.4, 53143.2,
        43349.9, 45938.6, 49371.2, None  # 2025 IV квартал отсутствует
    ]

    # Экстраполяция IV квартала 2025 с помощью линейной регрессии по трём точкам
    x = np.array([1, 2, 3])          # номера кварталов внутри 2025 года
    y = np.array(raw_numbers[-4: -1])
    raw_numbers[-1] = np.polyval(np.polyfit(x, y, deg=1), 4)

    # Годы и кварталы
    years = list(range(2011, 2026))
    quarters = ['Q1', 'Q2', 'Q3', 'Q4']

    # Создаем индекс из начала кварталов
    dates = []
    values = []
    idx = 0
    for year in years:
        for q in quarters:
            if idx < len(raw_numbers) * 4 * 4:
                # Дата начала квартала: 1 января, 1 апреля, 1 июля, 1 октября
                if q == 'Q1':
                    date = pd.Timestamp(year=year, month=1, day=1)
                elif q == 'Q2':
                    date = pd.Timestamp(year=year, month=4, day=1)
                elif q == 'Q3':
                    date = pd.Timestamp(year=year, month=7, day=1)
                else:  # Q4
                    date = pd.Timestamp(year=year, month=10, day=1)
                dates.append(date)
                values.append(raw_numbers[idx] / 4)
                idx += 1
            else:
                break
        if idx >= len(raw_numbers):
            break

    # Создаем Series с квартальной частотой
    gva_quarterly = pd.Series(values, index=dates)

    # Передискретизация на месяцы (начало месяца) с линейной интерполяцией
    gva_monthly = gva_quarterly.resample('MS').interpolate(method='linear')

    # Формируем результат
    result = pd.DataFrame({
        'Date': gva_monthly.index,
        'Value': gva_monthly.values
    }).reset_index(drop=True)

    return result

